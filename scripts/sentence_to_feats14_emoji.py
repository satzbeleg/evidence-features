# util
def clip_int8(x):
    return max(-128, min(127, x))


def find_percentile(f, brackets):
    for i, b in enumerate(brackets):
        if f <= b:
            return i
    return len(brackets) - 1


@ray.remote
def emoji_to_int8_wrapper(sent: str):
    # lookup sentiment scores, and frequency
    score_neg = [emojis.get(c, {}).get("neg", 0.0) for c in sent]
    score_pos = [emojis.get(c, {}).get("pos", 0.0) for c in sent]
    score_neut = [emojis.get(c, {}).get("neut", 0.0) for c in sent]
    freqs = [emojis.get(c, {}).get("freq", None) for c in sent]
    # remove all scores below 0.01
    score_neg = [s for s in score_neg if s > 0.01]
    score_pos = [s for s in score_pos if s > 0.01]
    score_neut = [s for s in score_neut if s > 0.01]
    freqs = [f for f in freqs if f is not None]
    # assign to decentile
    quantiles_neg = [find_percentile(s, brackets_neg) for s in score_neg]
    quantiles_pos = [find_percentile(s, brackets_pos) for s in score_pos]
    quantiles_neut = [find_percentile(s, brackets_neut) for s in score_neut]
    quantiles_freq = [find_percentile(f, brackets_freq) for f in freqs]
    # count decentiles neg
    cnt_neg = [0] * 6
    for q in quantiles_neg:
        cnt_neg[q] += 1
    # count decentiles pos
    cnt_pos = [0] * 6
    for q in quantiles_pos:
        cnt_pos[q] += 1
    # count decentiles neut
    cnt_neut = [0] * 6
    for q in quantiles_neut:
        cnt_neut[q] += 1
    # count decentiles
    cnt_freq = [0] * 4
    for q in quantiles_freq:
        cnt_freq[q] += 1
    # done
    return (
        len(sent.split(" ")),  # num tokens
        *[clip_int8(c) for c in cnt_neg],
        *[clip_int8(c) for c in cnt_pos],
        *[clip_int8(c) for c in cnt_neut],
        *[clip_int8(c) for c in cnt_freq]
    )


def emoji_to_int8(sentences: List[str]):
    try:
        return ray.get([
            emoji_to_int8_wrapper.remote(sent)
            for sent in sentences])
    except Exception as e:
        logger.error(e)
        ray.shutdown()
        gc.collect()


